#include <unistd.h>

void	ft_putnbr(int nb);

void	ft_putnbr(int nb)
{
	if (nb < -2147483647)
		write(1, "-2147483648", 10);
	else if (nb < 0)
	{	
		nb = -nb;
		write(1, "-", 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	write(1, &"0123456789"[nb % 10], 1);
}
